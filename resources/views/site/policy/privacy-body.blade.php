## Section 2 | Policy

### 2.1 Registration

To use our services, you must be a registered and active VATSIM member, except for certain public parts of our system.

- Access via VATSIM Single Sign-On (SSO) shares your data with us after consent.
- Some data is collected without login (e.g. cookies, IP addresses).
- Google Analytics is used and may collect personally identifiable information.

### 2.2 Information Collection and Use

- VATSIM UK retains data provided by VATSIM and any supplementary data it collects.
- Third parties may store data necessary to provide services.
- Data is never sold or rented without permission.

#### Responsibilities:
1. Be transparent about data collection.
2. Collect/process data only on lawful basis (legitimate interest).
3. Manage data per internal policies.
4. Report breaches promptly.

#### User Rights:
- Right to be informed, access, rectify, erase, restrict processing, and object.

> Contact: [sar@vatsim.uk](mailto:sar@vatsim.uk) / [privacy@vatsim.uk](mailto:privacy@vatsim.uk)

### 2.3 Personal Statistical Information

Collected via [Google Analytics](https://support.google.com/analytics/answer/6004245?hl=en).

### 2.4 Cookies

Cookies are used for tracking and security, including from Google Analytics and live chat systems.

### 2.5 Log Files

- IP addresses are logged to analyse trends and enhance security.
- May be linked to individuals during investigations.

### 2.6 Sharing of Certain Information

- Aggregated demographic data may be shared (non-identifiable).
- Backups are encrypted and stored via [Amazon Web Services](https://aws.amazon.com/privacy/).

### 2.7 Links to Outside Websites

VATSIM UK is not responsible for the privacy practices of linked websites.

### 2.8 Required Subscriptions

- No mailing list subscription is required.
- Mandatory emails relate only to service operation.
- Unwanted emails can be reported to [privacy@vatsim.uk](mailto:privacy@vatsim.uk).

### 2.9 Surveys, Contests and Marketing

- Participation is voluntary.
- Data may include name, address, age, etc.
- Used to notify winners or improve services.

### 2.10 Security

- Data access is restricted to authorised staff.
- Controlled by internal policies and agreements.

### 2.11 Minimum Age Requirement

- VATSIM ensures members meet the legal age of consent.
- VATSIM UK ensures valid consent is recorded before allowing access.

