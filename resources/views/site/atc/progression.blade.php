                        Student 1 (S1) -&gt; Student 2 (S2)
                    </h2>

                    <p>
                        Your training now moves onto Tower, and the skills required as a tower controller will be
                        steadily introduced to
                        you. Your training then continues until you are at the required standard in all aspects
                        of the aerodrome
                        criteria, when you will be forwarded for your Tower exam.
                    </p>

                    <p>
                        This will be carried
                        out by a VATSIM UK examiner, who will be assessing you on the following areas:
                    </p>

                    <ul>
                        <li>
                            Ability to decode METARs
                        </li>
                        <li>
                            Understanding local operating procedures for the airfield in order to provide satisfactory
                            service and
                            co-ordination to adjacent units
                        </li>
                        <li>
                            Understanding local handoff procedures for departing traffic
                        </li>
                        <li>
                            Controlling minimum of two aircraft in the VFR circuit
                        </li>
                        <li>
                            Communication and co-ordination with adjacent controllers
                        </li>
                        <li>
                            Understanding Tower area of responsibility
                        </li>
                        <li>
                            Safe separation on the ground and in the air
                        </li>
                        <li>
                            Ability to apply reasonable expedition consistent with safety
                        </li>
                        <li>
                            Understanding the IFR missed approach procedure
                        </li>
                        <li>
                            Go-arounds and where to use them
                        </li>
                        <li>
                            Taxi instructions
                        </li>
                        <li>
                            VFR aircraft joining and departing the field
                        </li>
                        <li>
                            Issuing IFR departure clearances complying with local SIDs and noise abatement procedures
                        </li>
                        <li>
                            Standard phraseology in accordance with CAP413
                        </li>
                    </ul>

                    <p>
                        The exam shouldn&#39;t last any longer than 90 mins, and the examiner will be on hand to help
                        you if you have
                        any issues. At the end of the exam, you will be told whether you have passed or not. If you have
                        passed,
                        congratulations! You will now be upgraded to Student (S2). After the exam, you will receive
                        detailed feedback
                        through the RTS System on where you performed well and where you were a little weaker. Remember:
                        perfection is
                        not what is being looked for, but an ability to build on your existing experience and knowledge.
                    </p>

                    <p>
                        If you are not successful in the exam, the examiner will advise you whether you need some more
                        training before
                        re-attempting the exam. There is no minimum waiting period between practical exams.
                    </p>

