                    </p>

                    <ul>
                        <li>
                            Airspace classifications
                        </li>
                        <li>
                            Air Traffic Services Outside Controlled Airspace (ATSOCAS)
                        </li>
                        <li>
                            Wake separation standards
                        </li>
                        <li>
                            Aircraft limitations and performance
                        </li>
                        <li>
                            Effects of weather on aircraft performance
                        </li>
                        <li>
                            Stack management
                        </li>
                        <li>
                            METAR knowledge
                        </li>
                    </ul>

                    <p>
                        There are a number of resources on the VATSIM UK site which can help you to study these areas;
                        your mentor will
                        also be happy to help.
                    </p>

                    <p>
                        As with your Tower training, once your mentor deems you to be at the required standard,
                        you will be forwarded to take your practical exam, which is carried out by a VATSIM UK examiner.
