                        Theory Exams
                    </h2>

                    <p>
                        In order to progress through the controller ratings, you will need to take a series of theory
                        exams along the
                        way as part of your training. The following is standard across all theory exams:
                    </p>

                    <ul>
                        <li>
                            The exam will consist of 20 multiple choice questions selected at random from the database
                        </li>
                        <li>
                            The pass mark for all theory exams is 80%
                        </li>
                        <li>
                            You will be advised of the result of your exam immediately upon completion, together with a
                            list of
                            questions which you didn&#39;t answer correctly
                        </li>
                        <li>
                            You have 20 minutes to complete the exam. If your web browser supports Javascript a
                            countdown timer will
                            show your time remaining
                        </li>
                        <li>
                            Ensure you answer every question; unanswered questions will score as incorrect answers but
                            will not deduct
                            points from your score
                        </li>
                        <li>
                            If you fail the exam there is a 7 day waiting period before you may retake the exam, use
                            this time to read
                            up on the topics you were weaker on
                        </li>
                        <li>
                            It is only possible to attempt the theory exam for one level above your current rating i.e.
                            a Student 2 can
                            only attempt the Senior Student (S3) exam
                        </li>
                        <li>
                            <p>
                                You must register a pass in the theory exam for the appropriate level before you can be
                                submitted for
                                the practical exam
                            </p>
                        </li>
                    </ul>

