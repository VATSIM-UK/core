                    <h3>Removal of CTS Permissions</h3>
                    
                    <p>
                        Members who signed up for The Flying Programme will have their CTS permissions revoked after the following time has elapsed:
                    </p>
                    
                    <ul>
                        <li style="margin-left:18pt;">
