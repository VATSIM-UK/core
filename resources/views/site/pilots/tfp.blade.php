                    <p>
                        The Moodle assessment is open book, with a time limit of 1 hour and a pass grade of 75%. The exam may
                        be retaken after a cooldown period, allowing you to revise the necessary areas.
                    </p>

                    <h3>One-on-One Sessions</h3>

                    <p>
                        One-on-one sessions can be requested via CTS once a member has been granted permissions by the
                        Initial Flight Instructor. Once a session is booked, the mentor will communicate with the student in
                        Discord to select a route and coordinate any further details for the session.
                    </p>

                    <p>
                        A student can request an <b>IFR route</b> of their choice, or recommended by the mentor.
                        These routes should have a short duration (around 1 hour) and relatively simple procedures.
                    </p>

                    <h3>Group Sessions</h3>

                    <h4>Group Flights</h4>

                    <p>
                        Group flights will be held periodically subject to demand and announced in the VATSIM UK Discord.
                        Members wishing to attend the group flight must enrol via CTS to be allocated a slot.
                        Members who have not enrolled via CTS are still welcome to attend the group flight
                        and be in the Discord channel.
                    </p>

                    <p>
                        Group flights will take place in the "tfp-group-flight" Discord channel with at least
                        one mentor present. The mentor is there to help everyone attending but will give priority to helping
                        members who booked via CTS.
                    </p>

                    <h3>Sign-Off</h3>

                    <p>
                        Members who have completed The Flying Programme will be announced weekly in the VATSIM UK Discord.
                        To quality, members will have to attend:
                    </p>

                    <ul>
                        <li style="margin-left:18pt;">
                            At least 1 one-on-one session
                        </li>
                        <li style="margin-left:18pt;">
                            Be deemed by a Flying Programme mentor to have sufficient understanding of:
                        </li>
                        <li style="margin-left:54pt;">
                            Radiotelephony
                        </li>
                        <li style="margin-left:54pt;">
                            Aircraft and Systems
                        </li>
                        <li style="margin-left:54pt;">
                            Flight Planning
                        </li>
                    </ul>

                    <h3>Removal of CTS Permissions</h3>

                    <p>
                        Members who signed up for The Flying Programme will have their CTS permissions revoked after the
                        following time has elapsed:
                    </p>

                    <ul>
                        <li style="margin-left:18pt;">
                            One-on-one sessions: 90 days without a session request, or after 2 session reports have been
