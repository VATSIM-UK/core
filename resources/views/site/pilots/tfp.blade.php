                            Altimetry
                        </li>
                    </ul>
                    
                    <p>
                        The final assessment is open book, with a time limit of 1 hour and a pass grade of 75%. The exam may be retaken after a cooldown period, allowing you to revise the necessary areas.
                    </p>
                    
                    <h3>One-on-One Sessions</h3>
                    
                    <p>
                        One-on-one sessions can be requested via CTS once a member has been granted permissions by the Initial Flight Instructor. Once a session is booked, the mentor will communicate with the student in Discord to select a route and coordinate any further details for the session.
                    </p>
                    
                    <p>
                        A student can request an <b>IFR route</b> from our list of beginner friendly flights, available as a pinned comment in the students‚Äô channel. These routes have been planned and verified by the Pilot Training team for their short duration (under 1 hour flight time) and simple procedures (eg SIDs, STARs).
                    </p>
                    
                    <p>
                        <b>A member can request a maximum of <i>TWO</i> one-on-one sessions per 12 months, with the 12 months starting from the date of the second session.</b> This allows a member to return after absence from the network and get assistance in starting to fly again. The Initial Flight Instructor may allow exceptions to this rule in very specific circumstances.
                    </p>
                    
                    <h3>Group Sessions</h3>

                    <h4>Group Flights</h4>
                    
                    <p>
                        Group flights will be flown once every two weeks (depending on the demand) along one of the routes as listed in the student‚Äôs channel.
                    </p>
                    
                    <p>
                        Members with the relevant Flying Programme permissions to book a slot in CTS and will be allocated a specific flight number for the flight, with the ficticious callsign ‚Äò<b>TANGENT</b>‚Äô. Members who have not enrolled via CTS are still welcome to attend the group flight and be in the Discord channel.
                    </p>
                    
                    <p>
                        Group flights will take place in one of the public group_flight_1/2/3 Discord channels with at least one mentor present. The mentor is there to help everyone attending but will give priority to helping members who booked via CTS.
                    </p>
                    
                    <p>
                        Members taking part in The Flying Programme can attend an unlimited amount of group flights.
                    </p>
                    
                    <h4>Seminars</h4>
                    
                    <p>
                        Group seminars will be hosted regularly on a rotating series of topics. Additionally, event-specific briefings for events in the UK such a <i>Cross the Pond</i> will be held.
                    </p>
                    
                    <h3>Sign-Off</h3>
                    
                    <p>
